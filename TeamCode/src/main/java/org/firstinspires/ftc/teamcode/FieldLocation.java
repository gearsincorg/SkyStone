package org.firstinspires.ftc.teamcode;

/**
 * Created by Will on 12/17/2016.
 * This is an enumerated list of Field Locations.
 * The S prefix indicates a Start position
 * The D prefix indicates a Destination
 */
public enum FieldLocation
    {
    NONE,
    S_RAMP,
    S_COLLECT,
    S_SAFE,
    D_NEAR_BEACON,
    D_FAR_BEACON,
    D_SHOOT,
    D_BALL,
    D_CENTER,
    D_AWAY,
    D_RAMP;
}
